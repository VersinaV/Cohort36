package lesson_46.classwork;

public class Variables {
  /*
  Opisati po odnoi peremenoi kajdowo primitiwnowo tipa danih
  i wiwesti ih na ekran
   */
  public static void main(String[] args) {
    byte number1 = 12;
    short number2 = 16555;
    int number3 = 100000;
    long number4 = 123456789123L;
    float number5 = 45.3f;
    double number6 = 456.789546;
    boolean flag = false;
    char letter = 'a';
    String text = "Hello";

    System.out.println("Byte number: " + number1);
    System.out.println("short number: " + number2);
    System.out.println("int number: " + number3);
    System.out.println("long number: " + number4);
    System.out.println("float number: " + number5);
    System.out.println("double number: " + number6);
    System.out.println("boolean flag: " + flag);
    System.out.println("char symbol: " + letter);
    System.out.println("String text: " + text);
    /*
    byte
    short
    int
    long
    boolean
    float
    doable
    char
    String
     */
  }
}
